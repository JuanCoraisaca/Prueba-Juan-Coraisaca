     private void actualizarTable1(){
        
        String [] encabezado = new String[4];
        encabezado[0]="CÃ³digo";
        encabezado[1]="Nombre";
        encabezado[2]="Apellido";
        encabezado[3]="Cedula";
        
        
        
        var datos = new Object[this.profesorControl.listar().size()][4];
        
        var i=0;
        for(var profesor:this.profesorControl.listar())
        {
            datos[i][0]=profesor.getCodigo();
            datos[i][1]=profesor.getNombre();
            datos[i][2]=profesor.getApellido();
            datos[i][3]=profesor.getCedula();
            
            i++;
        } 
        
     
        this.modeloTabla = new DefaultTableModel(datos,encabezado);
        this.jTable1.setModel(modeloTabla);
        
        }
